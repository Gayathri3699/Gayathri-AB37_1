package assignments;

public class ManyStatics 
{      
	     static void dinner()
	     {
	    	 System.out.println("i had my dinner");
	     }
	     static void movie()
	     {
	    	 System.out.println("I like to watch movies");
	     }
	     static void friend()
	     {
	    	 System.out.println("yes she is my friend");
	     }
	     static void welcome()
	     {
	    	 System.out.println("you are welcome");
	     }

	public static void main(String[] args) {
	  dinner();
	  movie();
	  friend();
	  welcome();

	}

}
