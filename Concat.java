package stringfunctions;

public class Concat 
{

	public static void main(String[] args)
	{
      String fname = "gayathri";
      String lname = "rao";
     
      System.out.println(fname.concat(lname));
      System.out.println((fname.toUpperCase()));
      System.out.println(fname.concat(" ").concat(lname));
	}

}










